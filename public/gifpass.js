document.getElementById("div0")
     .addEventListener("click", ee => {
          ee.preventDefault();
          document.getElementById("gifatt").innerHTML = "";
          document.getElementById("urlatt").innerHTML = "";
          let gif = document.createElement("img");
          let link = document.createElement("input");
          let but = document.createElement("button");
          gif.src = document.getElementById("gif0").src;
          link.id = "post_gif";
          link.value = document.getElementById("gif0").src;
          link.name = "post_gif";
          link.type = "text";
          link.style = "visibility: hidden";
          but.type = "button";
          but.class = "close";
          but.aria_label = "Close";
          but.id = "butatt";
          gifatt.appendChild(gif);
          urlatt.appendChild(link);
          gifatt.appendChild(but);
          window.alert("Gif attached!")

     });

